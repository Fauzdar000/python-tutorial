thislist =["rock", "paper", "scissors"]
print(thislist[1])  #accessing elements in the list
print(thislist[-1])  #accessing elements from the end of the list
print(thislist[0:2])  #accessing a range of elements in the list
print(thislist[:2])  #accessing elements from the start to a specific index
print(thislist[1:])  #accessing elements from a specific index to the end